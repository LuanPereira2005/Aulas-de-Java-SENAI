// Criacao de interface.
// A interface e um tipo de classe criada apenas quando existem metodos em comum a outras classes.
// A interface permite implementar apenas a assinatura do metodo e nao o metodo em si, 
// obrigando as classes que implementarem da interface, implementar os metodos.
public interface AreaCalculavel {

	public double calcularArea(double x, double y);

}
