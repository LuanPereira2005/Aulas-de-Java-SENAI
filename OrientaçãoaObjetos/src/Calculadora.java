
public class Calculadora {

	public static void main(String[] args) {
		// Criacao de variaveis
		double n1 = 25000;
		double n2 = 50;
		double resulSo;
		double resulSu;
		double resulM;
		double resulD;

		// Operacoes matematicas basicas e imprimir na tela os resultados
		resulSo = n1 + n2;
		System.out.println("O resultado da soma é:" + resulSo);
		resulSu = n1 - n2;
		System.out.println("O resultado da subtração é:" + resulSu);
		resulM = n1 * n2;
		System.out.println("O resultado da divisão é:" + resulM);
		resulD = n1 / n2;
		System.out.println("O resultado da multiplicação é:" + resulD);

	}

}
